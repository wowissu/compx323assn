public class Event {
    public String className;
    public String locationRoom;
    public String eventType;
    public String eventTime;

    public void setClassName (String val) {
        className = val;
    }
    public void setLocationRoom (String val) {
        locationRoom = val;
    }
    public void setEventType (String val) {
        eventType = val;
    }
    public void setEventTime (String val) {
        eventTime = val;
    }
}
